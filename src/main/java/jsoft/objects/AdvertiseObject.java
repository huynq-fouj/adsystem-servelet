package jsoft.objects;

public class AdvertiseObject {
	private short advertise_id; 
	private String advertise_title;
	private String advertise_url;
	private String advertise_notes;
	private byte advertise_type;
	private String advertise_display;
	private String advertise_image;
	public short getAdvertise_id() {
		return advertise_id;
	}
	public String getAdvertise_title() {
		return advertise_title;
	}
	public String getAdvertise_url() {
		return advertise_url;
	}
	public String getAdvertise_notes() {
		return advertise_notes;
	}
	public byte getAdvertise_type() {
		return advertise_type;
	}
	public String getAdvertise_display() {
		return advertise_display;
	}
	public String getAdvertise_image() {
		return advertise_image;
	}
	public void setAdvertise_id(short advertise_id) {
		this.advertise_id = advertise_id;
	}
	public void setAdvertise_title(String advertise_title) {
		this.advertise_title = advertise_title;
	}
	public void setAdvertise_url(String advertise_url) {
		this.advertise_url = advertise_url;
	}
	public void setAdvertise_notes(String advertise_notes) {
		this.advertise_notes = advertise_notes;
	}
	public void setAdvertise_type(byte advertise_type) {
		this.advertise_type = advertise_type;
	}
	public void setAdvertise_display(String advertise_display) {
		this.advertise_display = advertise_display;
	}
	public void setAdvertise_image(String advertise_image) {
		this.advertise_image = advertise_image;
	}
	
}
